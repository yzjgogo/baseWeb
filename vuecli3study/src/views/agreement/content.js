const joinSingleHTML = (item) => {
  let selfContent = `<${item.tag} class=${item.className || ''}>${
    item.content
  }</${item.tag}>`
  if (item.children) {
    item.children.forEach((_i) => {
      selfContent += joinSingleHTML(_i)
    })
    return `<section>${selfContent}</section>`
  } else {
    return selfContent
  }
}

export const joinContent = (type) => {
  const contentArray = agreementData[type].content
  let innerHTML = ''
  for (let i = 0; i < contentArray.length; i++) {
    const item = contentArray[i]
    const itemHTML = joinSingleHTML(item)
    innerHTML += itemHTML
  }
  return innerHTML
}
